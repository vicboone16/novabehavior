// Helper functions
export function getAllABLLSRItems(): ABLLSRItem[] {
  return ABLLS_R_DOMAINS.flatMap(d => d.items);
}

export function getABLLSRDomain(code: string): ABLLSRDomain | undefined {
  return ABLLS_R_DOMAINS.find(d => d.code === code);
}

export function getABLLSRTotalItemCount(): number {
  return ABLLS_R_DOMAINS.reduce((sum, d) => sum + d.items.length, 0);
}

export function getABLLSRMaxPossibleScore(): number {
  return ABLLS_R_DOMAINS.reduce(
    (sum, d) => sum + d.items.reduce((s, item) => s + item.maxScore, 0),
    0
  );
}

// Mastery labels matching the Excel dashboard
export function getABLLSRMasteryLabel(percent: number): string {
  if (percent === 0) return 'Not Started';
  if (percent < 50) return 'Emerging';
  if (percent < 80) return 'Developing';
  return 'Mastered';
}
